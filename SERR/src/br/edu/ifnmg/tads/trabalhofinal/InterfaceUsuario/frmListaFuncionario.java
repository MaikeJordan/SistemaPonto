    private void preenchetabela(List<Funcionario> funcionarios){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Código");
        model.addColumn("Nome");
        model.addColumn("RG");
        model.addColumn("CPF");
        model.addColumn("SIAPE");
        model.addColumn("Cargo");
        model.addColumn("Usuário");
        for (Funcionario funcionario : funcionarios){
            Vector v = new Vector();
            pessoa = new Pessoa();
            pessoa = pessoadao.Abrir(funcionario.getCodpessoa());
            funcionario.setPessoa(pessoa);
            int us = usuariodao.VerificaUsuario(funcionario.getCodfuncionario());
            v.add(0, funcionario.getCodfuncionario());
            v.add(1, funcionario);
            v.add(2, pessoa.getRg());
            v.add(3, pessoa.getCpf());
            v.add(4, funcionario.getSiape());
            v.add(5, funcionario.getCargo());
            if (us > 0){
                v.add(6, "Sim");
            } else {
                v.add(6, "Não");
            }
            model.addRow(v);
        }
        
        tblFuncionarios.setModel(model);
    }
    
