    private void btnRemoverFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverFuncionarioActionPerformed
        // TODO add your handling code here:
        if (tblFuncionarios.getSelectedRow() >=0){

            funcionarios = new LinkedList<>();
            funcionarios = funcionariodao.ListarTodos();
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja Apagar esse Professor?") == 0){
                Funcionario funcionario = (Funcionario) tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 1);

                pessoa = pessoadao.Abrir(funcionario.getCodpessoa());
                pessoa.setEmails(emaildao.Abrir(funcionario.getCodpessoa()));
                pessoa.setEnderecos(enderecodao.Abrir(funcionario.getCodpessoa()));
                pessoa.setTelefones(telefonedao.Abrir(funcionario.getCodpessoa()));

                for (Endereco en : pessoa.getEnderecos()){
                    enderecodao.Apagar(en.getCodendereco());
                }

                for (Telefone tel : pessoa.getTelefones()){
                    telefonedao.Apagar(tel.getCodtelefone());
                }

                for (Email em : pessoa.getEmails()){
                    emaildao.Apagar(em.getCodemail());
                }

                if (funcionariodao.Apagar(funcionario.getCodfuncionario()) && pessoadao.Apagar(pessoa.getCodpessoa())){
                    funcionarios.remove(funcionario);
                    JOptionPane.showMessageDialog(rootPane, "Funcionário com Sucesso!");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Ação Cancelada!");
            }

            preenchetabela(funcionarios);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Funcionário por favor!");
        }
    }//GEN-LAST:event_btnRemoverFuncionarioActionPerformed

    private void btnRemoverUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverUsuarioActionPerformed
        // TODO add your handling code here:
        if (tblFuncionarios.getSelectedRow() >= 0){
            Funcionario funcionario = (Funcionario) tblFuncionarios.getValueAt(tblFuncionarios.getSelectedRow(), 1);
            if (usuariodao.VerificaUsuario(funcionario.getCodfuncionario()) > 0){
                int us = usuariodao.VerificaUsuario(funcionario.getCodfuncionario());
                if(usuariodao.Apagar(us))
                JOptionPane.showMessageDialog(rootPane, "Usuário Apagado com Sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Este funcionário não possui um usuário!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Funcionário por favor!");
        }
    }//GEN-LAST:event_btnRemoverUsuarioActionPerformed

