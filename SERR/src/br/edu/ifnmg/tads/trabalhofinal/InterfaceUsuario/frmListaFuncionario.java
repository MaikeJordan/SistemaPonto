    private void btnBuscarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarFuncionarioActionPerformed
        // TODO add your handling code here:
        Funcionario funcionario = new Funcionario();
        Pessoa pessoa = new Pessoa();

        if (cbxfiltro.getSelectedIndex() == 0){
            funcionario.setCodfuncionario(Integer.parseInt(txtFiltro.getText()));
            funcionario.setPessoa(pessoa);
        }

        if (cbxfiltro.getSelectedIndex() == 1){
            pessoa.setNome(txtFiltro.getText());
            funcionario.setPessoa(pessoa);
        }

        if (cbxfiltro.getSelectedIndex() == 2){
            pessoa.setRg(txtFiltro.getText());
            funcionario.setPessoa(pessoa);
        }

        if (cbxfiltro.getSelectedIndex() == 3){
            pessoa.setCpf(Integer.parseInt(txtFiltro.getText()));
            funcionario.setPessoa(pessoa);
        }

        if (cbxfiltro.getSelectedIndex() == 4){
            funcionario.setSiape(txtFiltro.getText());
            funcionario.setPessoa(pessoa);
        }

        if (cbxfiltro.getSelectedIndex() == 5){
            funcionario.setCargo(txtFiltro.getText());
            funcionario.setPessoa(pessoa);
        }

        List<Funcionario> funcionarios2 = funcionariodao.BuscarFuncionario(funcionario);
        List<Pessoa> pessoas = pessoadao.BuscarPessoa(funcionario.getPessoa());
        List<Funcionario> func = new LinkedList<>();

        for (Funcionario fu : funcionarios2){
            for (Pessoa pe : pessoas){
                if (fu.getCodpessoa() == pe.getCodpessoa()){
                    fu.setPessoa(pe);
                    func.add(fu);
                }
            }
        }

        preenchetabela(func);

    }//GEN-LAST:event_btnBuscarFuncionarioActionPerformed

