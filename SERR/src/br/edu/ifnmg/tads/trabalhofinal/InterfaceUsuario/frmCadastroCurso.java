    private void adicionadiscitable() {

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome");
        model.addColumn("Ementa");
        for (Disciplina d : curso.getDisciplinas()) {
            Vector v = new Vector();
            v.add(d.getNome());
            v.add(d.getEmenta());
            model.addRow(v);
        }

        tblCursos.setModel(model);
    }

    private void LimpaCampos() {

        txtNomeDisc.setText("");
        txtEmenta.setText("");

    }

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(RootPane, "Deseja adicionar Curso?") == 0) {        
            try {
                curso.setNome(txtNome.getText());
                curso.setDuracao(Integer.parseInt(txtQtd.getText()));                               
            }catch (Exception ex) {
                Logger.getLogger(frmCadastroCurso.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(cursodao.Salvar(curso)){
                curso.setCodcurso(cursodao.Consultacodcurso());
                for(Disciplina d : curso.getDisciplinas()){
                    disc = new Disciplina(0, d.getNome(), d.getEmenta(), curso);
                    System.out.println(disc);
                    disciplinadao.Salvar(disc);
                }
            }
            cursodao.Salvar(curso);
            JOptionPane.showMessageDialog(RootPane, "Curso Cadastrado com Sucesso!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(RootPane, "Deseja adicionar Disciplina?") == 0) {
            disc = new Disciplina();
            try {
                disc.setNome(txtNomeDisc.getText());
                disc.setEmenta(txtEmenta.getText());
                disc.setCurso(curso);
            } catch(Exception ex) {
                Logger.getLogger(frmCadastroCurso.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if (curso.getDisciplinas().contains(disc)) {
                    JOptionPane.showMessageDialog(RootPane, "Disciplina já adicionado!");
            } else {
                curso.addDisc(disc);
                adicionadiscitable();
                LimpaCampos();
                JOptionPane.showMessageDialog(RootPane, "Disciplina adicionada com sucesso!");
            }
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(tblCursos.getSelectedRow() >= 0) {
            if (JOptionPane.showConfirmDialog(RootPane, "Deseja remover esta Disciplina?") == 0){
                Disciplina disciplinaselect = (Disciplina) tblCursos.getValueAt(tblCursos.getSelectedRow(), 1);
                System.out.println(disciplinaselect);
                if(curso.getDisciplinas().contains(disciplinaselect)){
                   curso.removeDisciplina(disciplinaselect);
                }
                JOptionPane.showMessageDialog(RootPane, "Disciplina removida com sucesso!");
                adicionadiscitable();
                LimpaCampos();
            } else {
            JOptionPane.showMessageDialog(RootPane, "Selecione um email por favor!");
            }
        }
        
    }//GEN-LAST:event_btnDelActionPerformed

